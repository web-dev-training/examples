<?php

/*
 * store a string a variable 
 */
$var_string_1 = 'My name is : ';
$var_string_2 = 'Bob!';

/*
 * concatenate and store the values of two variables in 
 * a third variable using 
 * string concatenation operator '.'
 */
$var_string_3 = $var_string_1 . $var_string_2;

/*
 * print the values
 */
print $var_string_1 . "\n"; // My name is :
print $var_string_2 . "\n"; // Bob!
print $var_string_3 . "\n"; // My name is : Bob!
